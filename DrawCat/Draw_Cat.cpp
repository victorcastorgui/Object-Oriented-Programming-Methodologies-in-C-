// Draw_Cat.cpp
//
// When this program is run, it should print the following lines on
// the console.
//
//  -------
// | /\_/\ |
// |( o o )|
// | > ^ < |
//  -------
//Schrodinger
//

#include <iostream>

using namespace std;

void draw_cat()
{
    std::cout << "  -------\n";
    std::cout << " | /\\_/\\ |\n";
    std::cout << " |( o o )|\n";
    std::cout << " | > ^ < |\n";
    std::cout << "  -------\n";
    std::cout << "Schrodinger\n";
}

    
int main(int argc, const char * argv[])
{
    draw_cat();
    return 0;
}
